function inicializar_charts(charts){

    $( ".meter-gauge canvas" ).each(function( index ) {
        var container = $(this).get(0).getContext("2d");
        var data = {
            labels: [
                "Red",
                "Blue",
            ],
            datasets: [{
                data: [0,0],
                backgroundColor: [
                    'orange',
                    "#FFF",
                ],
                hoverBackgroundColor: [
                    'orange',
                    "#FFFFFF",
                ]
            }]
        };
        var chart = new Chart(container, {
            type: 'doughnut',
            data: data,
            resposive: true,
            options: {
                rotation: 1 * Math.PI,
                circumference: 1 * Math.PI,
                legend: {
                    display: false
                },
                tooltips: {
                    enabled: false
                },
                elements: {
                    center: {
                        text: '0%',
                        color: '#36A2EB', //Default black
                        fontStyle: 'Helvetica', //Default Arial
                        sidePadding: 15 //Default 20 (as a percentage)
                    }
                }
            }
        });
        var record_chart = {
            id : $(this).get(0).id,
            chart : chart,
        }


        charts.push(record_chart);
    });
    return charts;
}


